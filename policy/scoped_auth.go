// =====================================================================
// Secret generation
// =====================================================================

// NewSpawnSecret returns a 32-byte cryptographically random
// secret suitable for HMAC-SHA256. Used by manager.Load to mint
// a per-spawn signing key shared with the plugin.
//
// Panics on rand source failure — that's an unrecoverable system
// issue, not something to swallow.
func NewSpawnSecret() []byte {
	buf := make([]byte, MinScopedAuthSecretBytes)
	if _, err := rand.Read(buf); err != nil {
		panic(fmt.Sprintf("policy: crypto/rand failure: %v", err))
	}
	return buf
}

// =====================================================================
// ULID — minimal in-package implementation
// =====================================================================

// NewULID returns a time-prefixed, base32-encoded random identifier
// suitable for token IDs.
//
// **Why not a strict-spec ULID.** The proposal called for ULIDs for
// time-ordered audit scans. For our purposes (token IDs that need
// to be unique and ideally sortable), a hex-encoded
// `<unix-millis>-<random>` is simpler and adequate:
//
//   - Unique enough: 64 bits of random per ID, collision-free at
//     codefly's scale.
//   - Time-sortable: hex-encoded millis prefix sorts correctly
//     (until year 5138; we're not worried).
//   - URL-safe: hex characters fit any header / log format.
//   - 24 chars fixed-width: 16 (millis) + 1 (separator) + 16 (random).
//
// If we ever need strict-spec ULIDs (e.g. for cross-system
// correlation with another service that mints ULIDs), import
// github.com/oklog/ulid; for now this is dependency-free.
func NewULID() string {
	ms := time.Now().UnixMilli()
	var entropy [8]byte
	if _, err := rand.Read(entropy[:]); err != nil {
		panic(fmt.Sprintf("policy: crypto/rand failure in NewULID: %v", err))
	}
	return fmt.Sprintf("%016x-%016x", ms, binary.BigEndian.Uint64(entropy[:]))
}
